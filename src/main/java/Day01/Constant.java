package Day01;

/*
数据类型
    基本数据类型
        byte  1个字节
        short 2
        int 4
        long 8
        float 4
        double 8
        char 2
        boolean 1
    引用数据类型
         除去基本类型都是引用数据类型，指向一个对象，不是原始值
 */

public class Constant {
    // 这是变量 不是常量
    // 数据类型 变量名 = 数据值 ；

    /*
    同一个大括号里面 不能重名
    定义的变量 只有复制了才能使用
     */
    public static void main(String[] args) {
        byte b = 100;
        short s = 1000;
        int i = 123456;
        long l = 1234567890;
    }
}
