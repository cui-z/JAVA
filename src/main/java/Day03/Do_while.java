package Day03;

/*
  初始化表达式
      do {
      循环体
      步进表达式
      } while (布尔表达式);


一直循环次数推荐使用for  未知 while
 */

public class Do_while {
    public static void main(String[] args) {
        int i = 0;
        do {
            System.out.println("hello world");
            i++;
        } while (i<3);

    }
}
